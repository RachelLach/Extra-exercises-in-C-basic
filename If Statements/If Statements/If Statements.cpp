#include <iostream>
using namespace std; 

int main()
{
    int temperature = 30; 
    if (temperature < 15)   // In () we have a condition or a  boolean expression 
                            // If the expression is true so we print : 
        cout << "Cold!";    // This is part of if statement 
    cout << "Done!";        // This is not part of the if statement and always get executed!
    return 0; 
}



#include <iostream>
using namespace std; 

int main() {
    int numbers[] = { 1,2,3 };      // int array called number and initialize this to 1,2 and 3
    for (int i = 0; i < 3; i++)     // loop over this array and print each number on a new line 
        cout << numbers[i] << endl;
    return 0;
}


